package martioux.pro.grafco3.restbeans;

public class Users {
    String login, mdpmobile, nomusers, prenomusers, photousers, emailusers;
    int rowid;

    public Users() {
    }

    public String getLogin() {
        return login;
    }

    public void setLogin(String login) {
        this.login = login;
    }

    public String getMdpmobile() {
        return mdpmobile;
    }

    public void setMdpmobile(String mdpmobile) {
        this.mdpmobile = mdpmobile;
    }

    public String getNomusers() {
        return nomusers;
    }

    public void setNomusers(String nomusers) {
        this.nomusers = nomusers;
    }

    public String getPrenomusers() {
        return prenomusers;
    }

    public void setPrenomusers(String prenomusers) {
        this.prenomusers = prenomusers;
    }

    public String getPhotousers() {
        return photousers;
    }

    public void setPhotousers(String photousers) {
        this.photousers = photousers;
    }

    public String getEmailusers() {
        return emailusers;
    }

    public void setEmailusers(String emailusers) {
        this.emailusers = emailusers;
    }

    public int getRowid() {
        return rowid;
    }

    public void setRowid(int rowid) {
        this.rowid = rowid;
    }
}
